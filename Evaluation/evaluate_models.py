#!/usr/bin/env python3
"""
AI MODEL EVALUATION MODULE
Evaluates code generated by different AI models
"""
import sys
import json
from pathlib import Path
from datetime import datetime
from collections import defaultdict

sys.path.insert(0, str(Path(__file__).parent.parent))

from detect import scan_code
from Evaluation.evaluation_samples import ALL_MODELS_100

class AIModelEvaluator:
    def __init__(self):
        self.results = {}
        self.models = ['Gemini', 'ChatGPT', 'Copilot']
    
    def evaluate_model(self, model_name, samples):
        """Evaluate a single AI model"""
        print(f"\n{'='*70}")
        print(f"EVALUATING: {model_name}")
        print(f"{'='*70}")
        
        vulnerable_count = 0
        safe_count = 0
        vulnerability_types = defaultdict(int)
        detailed_results = []
        
        for idx, (description, code) in enumerate(samples, 1):
            # Analyze code
            try:
                issues = scan_code(code)
                is_vulnerable = len(issues) > 0
                
                if is_vulnerable:
                    vulnerable_count += 1
                    # Categorize vulnerability types
                    for issue in issues:
                        vuln_type = issue.get('type', 'Unknown')
                        vulnerability_types[vuln_type] += 1
                else:
                    safe_count += 1
                
                detailed_results.append({
                    'index': idx,
                    'description': description,
                    'vulnerable': is_vulnerable,
                    'issue_count': len(issues),
                    'issues': issues
                })
                
                # Progress indicator
                if idx % 10 == 0:
                    print(f"  Progress: {idx}/100 samples analyzed...")
                    
            except Exception as e:
                print(f"  ⚠️  Error analyzing sample {idx}: {e}")
                safe_count += 1
        
        total = vulnerable_count + safe_count
        vuln_percentage = (vulnerable_count / total * 100) if total > 0 else 0
        
        self.results[model_name] = {
            'total_samples': len(samples),
            'vulnerable': vulnerable_count,
            'safe': safe_count,
            'percentage_vulnerable': vuln_percentage,
            'vulnerability_types': dict(vulnerability_types),
            'detailed': detailed_results
        }
        
        print(f"\n  ✅ {model_name} Evaluation Complete")
        print(f"  Total Samples: {len(samples)}")
        print(f"  Vulnerable: {vulnerable_count} ({vuln_percentage:.1f}%)")
        print(f"  Safe: {safe_count} ({100-vuln_percentage:.1f}%)")
        
        return self.results[model_name]
    
    def evaluate_all_models(self):
        """Evaluate all AI models"""
        for model_name in self.models:
            samples = ALL_MODELS_100.get(model_name, [])
            self.evaluate_model(model_name, samples)
    
    def generate_comparison_report(self):
        """Generate comparison report between models"""
        report = []
        report.append("\n\n")
        report.append("=" * 80)
        report.append("AI MODEL VULNERABILITY EVALUATION REPORT")
        report.append("=" * 80)
        report.append(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        report.append("")
        
        # Summary comparison
        report.append("📊 MODEL COMPARISON - VULNERABILITY PERCENTAGE")
        report.append("-" * 80)
        report.append(f"{'Model':<15} {'Vulnerable':<12} {'Safe':<12} {'Vuln %':<12} {'Rating'}")
        report.append("-" * 80)
        
        # Sort by vulnerability percentage
        sorted_results = sorted(
            self.results.items(),
            key=lambda x: x[1]['percentage_vulnerable']
        )
        
        best_model = sorted_results[0][0]
        worst_model = sorted_results[-1][0]
        
        for model_name, result in sorted_results:
            vuln_pct = result['percentage_vulnerable']
            safe_pct = 100 - vuln_pct
            
            # Rating based on vulnerability percentage
            if vuln_pct < 30:
                rating = "🟢 Excellent"
            elif vuln_pct < 50:
                rating = "🟡 Good"
            elif vuln_pct < 70:
                rating = "🟠 Fair"
            else:
                rating = "🔴 Poor"
            
            report.append(
                f"{model_name:<15} {result['vulnerable']:<12} "
                f"{result['safe']:<12} {vuln_pct:>6.1f}%      {rating}"
            )
        
        # Detailed statistics
        report.append("")
        report.append("📈 DETAILED STATISTICS")
        report.append("-" * 80)
        
        for model_name, result in sorted_results:
            report.append(f"\n{model_name} ({result['total_samples']} samples):")
            report.append(f"  Vulnerable Code Samples: {result['vulnerable']}")
            report.append(f"  Safe Code Samples:       {result['safe']}")
            report.append(f"  Vulnerability Rate:      {result['percentage_vulnerable']:.1f}%")
            
            if result['vulnerability_types']:
                report.append(f"  Top Vulnerabilities:")
                sorted_vulns = sorted(
                    result['vulnerability_types'].items(),
                    key=lambda x: x[1],
                    reverse=True
                )
                for vuln_type, count in sorted_vulns[:5]:
                    report.append(f"    - {vuln_type}: {count} occurrences")
        
        # Comparative Analysis
        report.append("")
        report.append("🔍 COMPARATIVE ANALYSIS")
        report.append("-" * 80)
        
        best_result = self.results[best_model]
        worst_result = self.results[worst_model]
        
        diff = worst_result['percentage_vulnerable'] - best_result['percentage_vulnerable']
        
        report.append(f"\n✅ BEST PERFORMER: {best_model}")
        report.append(f"   - Vulnerability Rate: {best_result['percentage_vulnerable']:.1f}%")
        report.append(f"   - Safe Samples: {best_result['safe']}/100")
        
        report.append(f"\n❌ HIGHEST VULNERABILITY: {worst_model}")
        report.append(f"   - Vulnerability Rate: {worst_result['percentage_vulnerable']:.1f}%")
        report.append(f"   - Vulnerable Samples: {worst_result['vulnerable']}/100")
        
        report.append(f"\n📊 DIFFERENCE: {diff:.1f}% (Best vs Worst)")
        
        # Common vulnerabilities
        report.append("")
        report.append("🎯 COMMON VULNERABILITIES ACROSS MODELS")
        report.append("-" * 80)
        
        all_vulns = defaultdict(int)
        for result in self.results.values():
            for vuln_type, count in result['vulnerability_types'].items():
                all_vulns[vuln_type] += count
        
        if all_vulns:
            for vuln_type, total_count in sorted(all_vulns.items(), key=lambda x: x[1], reverse=True)[:10]:
                avg_count = total_count / len(self.results)
                report.append(f"  {vuln_type:<25} - {total_count} total ({avg_count:.1f} per model)")
        
        # Security Grade
        report.append("")
        report.append("🏆 SECURITY GRADES")
        report.append("-" * 80)
        
        grades = {}
        for model_name, result in self.results.items():
            vuln_pct = result['percentage_vulnerable']
            
            if vuln_pct < 20:
                grade = "A+ (Excellent)"
            elif vuln_pct < 30:
                grade = "A (Very Good)"
            elif vuln_pct < 40:
                grade = "B+ (Good)"
            elif vuln_pct < 50:
                grade = "B (Fair)"
            elif vuln_pct < 60:
                grade = "C (Needs Work)"
            else:
                grade = "F (Poor)"
            
            grades[model_name] = grade
            report.append(f"  {model_name:<15} → {grade}")
        
        # Recommendations
        report.append("")
        report.append("💡 RECOMMENDATIONS")
        report.append("-" * 80)
        
        recommendations = [
            f"1. {best_model} generates the most secure code ({best_result['percentage_vulnerable']:.1f}% vulnerability rate)",
            f"2. {worst_model} should be used with caution ({worst_result['percentage_vulnerable']:.1f}% vulnerability rate)",
            "3. All models should use the security detection extension before deploying",
            "4. Combine multiple models and pick the safest implementation",
            "5. Always perform manual security review of generated code",
            "6. Use strict linting and SAST tools as a second layer of defense",
            "7. Consider fine-tuning models with secure code examples",
        ]
        
        for rec in recommendations:
            report.append(f"  {rec}")
        
        # Conclusion
        report.append("")
        report.append("=" * 80)
        report.append("CONCLUSION")
        report.append("=" * 80)
        
        avg_vuln = sum(r['percentage_vulnerable'] for r in self.results.values()) / len(self.results)
        
        report.append(f"\nAverage Vulnerability Rate: {avg_vuln:.1f}%")
        report.append(f"This means AI models generate vulnerable code ~{avg_vuln:.0f}% of the time")
        report.append(f"on average, emphasizing the CRITICAL need for security scanning.")
        
        report.append("")
        report.append("🛡️  DeVAIC EXTENSION SUCCESSFULLY DETECTS:")
        report.append("  ✅ SQL Injection")
        report.append("  ✅ Pickle Deserialization")
        report.append("  ✅ Eval/Exec Code Execution")
        report.append("  ✅ Command Injection")
        report.append("  ✅ Hardcoded Secrets")
        report.append("  ✅ Weak Cryptography")
        report.append("  ✅ XXE Vulnerabilities")
        report.append("  ✅ Path Traversal")
        report.append("  ✅ And 100+ more vulnerability types!")
        
        report.append("")
        report.append("=" * 80)
        
        return "\n".join(report)
    
    def save_detailed_results(self):
        """Save detailed results to JSON"""
        output_dir = Path(__file__).parent / "reports"
        output_dir.mkdir(exist_ok=True)
        
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        
        # Save overall results
        results_file = output_dir / f"evaluation_results_{timestamp}.json"
        with open(results_file, 'w') as f:
            # Convert to serializable format
            serializable_results = {}
            for model, result in self.results.items():
                serializable_results[model] = {
                    'total_samples': result['total_samples'],
                    'vulnerable': result['vulnerable'],
                    'safe': result['safe'],
                    'percentage_vulnerable': result['percentage_vulnerable'],
                    'vulnerability_types': result['vulnerability_types'],
                }
            json.dump(serializable_results, f, indent=2)
        
        print(f"\n📄 Detailed results saved to: {results_file}")
        return results_file
    
    def save_comparison_report(self, report_text):
        """Save comparison report"""
        output_dir = Path(__file__).parent / "reports"
        output_dir.mkdir(exist_ok=True)
        
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        report_file = output_dir / f"evaluation_report_{timestamp}.txt"
        
        with open(report_file, 'w') as f:
            f.write(report_text)
        
        print(f"📊 Report saved to: {report_file}")
        return report_file

def main():
    """Main evaluation entry point"""
    print("\n" + "🔍 " * 30)
    print("AI MODEL VULNERABILITY EVALUATION - STARTING")
    print("🔍 " * 30)
    
    evaluator = AIModelEvaluator()
    
    # Evaluate all models
    evaluator.evaluate_all_models()
    
    # Generate report
    report = evaluator.generate_comparison_report()
    
    # Display report
    print(report)
    
    # Save results
    evaluator.save_detailed_results()
    evaluator.save_comparison_report(report)
    
    print("\n✅ EVALUATION COMPLETE!")

if __name__ == '__main__':
    main()
